import java.lang.*;

class variableDeclareation {
				public static void main(String args[]){
					int a = 5;
					short b = 130;
					char c = 'A';
					float d = 25.3f;
					double f = 50.80;
					System.out.println(a);
					System.out.println(b);
					System.out.println(c);
					System.out.println(d);
					System.out.println(f);
				}
}


